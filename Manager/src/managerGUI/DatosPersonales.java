    private void botonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActionPerformed
        // TODO add your handling code here:
        if (!botonPulsado) {
            //Si no esta pulsado significa que quieres editar la informacion del jugador
            nombre.setEditable(true);
            primerApellido.setEditable(true);
            segundoApellido.setEditable(true);
            annoNacimiento.setEditable(true);
            telefono.setEditable(true);
            email.setEditable(true);
            posicion.setEditable(true);
            boton.setText("Confirmar cambios");
            botonPulsado = true;
        } else {
            //Si ya habias pulsado el boton significa que quieres guardar los cambios
            manager.recuperarJugador().setNombre(nombre.getText().toLowerCase());
            manager.recuperarJugador().setPrimerApellido(primerApellido.getText().toLowerCase());
            manager.recuperarJugador().setSegundoApellido(segundoApellido.getText().toLowerCase());
            manager.recuperarJugador().setAnnoNacimiento(Integer.parseInt(annoNacimiento.getText()));
            manager.recuperarJugador().setTelefono(telefono.getText().trim());
            manager.recuperarJugador().setEmail(email.getText().trim());
            manager.recuperarJugador().setPosicion(posicion.getText().toUpperCase());
            //Despues de guardar los cambios hay que dejarlo todo como estaba 
            nombre.setEditable(false);
            primerApellido.setEditable(false);
            segundoApellido.setEditable(false);
            annoNacimiento.setEditable(false);
            telefono.setEditable(false);
            email.setEditable(false);
            posicion.setEditable(false);
            boton.setText("Editar informacion");
            botonPulsado = false;
            //Guardamos lo que hayamos editado
            manager.guardarDatosJugador();
        }


    }//GEN-LAST:event_botonActionPerformed

